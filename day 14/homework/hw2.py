# ლოგიკური ოპერატორები არის სიმბოლოები ან სიტყვები,
# რომლებიც გამოიყენება პირობის შესამოწმებლად და აბრუნებს True ან False


# 1.and
print (True and True)
print (True and False)
print (5 > 2 and 10 < 20)
print (3 == 3 and 4 != 5)

# 2.or
print(True or False)
print(False or False)
print(7 > 10 or 8 == 8)
print(5 != 5 or 9 > 2)

# 3.not 
print (not True)
print (not False)
print (not (5 > 2))
print (not (10 == 10))

