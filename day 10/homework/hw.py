# 2) კომენტარებით ახსენით რა არის input/output-ი და მოიყვანეთ მათი მაგალითები.


#input-ით ხდება მონაცემის შეყვანა კომპიუტერში. ინფუთის მაგალითებია:კლავიატურა, ხმის ჩაწერა, რაიმე ღილაკზე ხელის დაჭერა.
#output-ით კომპიუტერს მონაცემი გამოაქვს. აუთფუთის მაგალითებია: ხმა რომელიც დინამიკიდან გვესმის, მონიტორი, პრინტერი.
